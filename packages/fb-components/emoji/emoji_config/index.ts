import emojiEn from './emoji_en.json'
import emojiZh from './emoji_zh.json'

export const emojiConfig: Record<string, string> = {
  ...emojiEn,
  ...emojiZh,
}

export function getEmojiClass(name: string) {
  const emojiName = emojiConfig[name]
  if (!emojiName) return ''
  return `fb-emoji fb-emoji-${emojiName.split(' ').join('_')}`
}

